import math
#1.exp():is used to perform e^x operations,wher x is a number.
print(pow(math.e,10))  #without math module function
print(math.exp(10)) #with math module function

#2.log():used to perform logarithic operation
print(round(math.pow(5,2)))
print(math.log(8,2)) #syntax:log(8,2)

#3.suare-->sqrt()
print(math.sqrt(25))

##Trignometric functions-->sin,cos,tan
print(round(math.sin(0)))
print(round(math.cos(0)))
print(math.tan(1))
print(math.radians(90))